-- 1. 기존 테이블 깔끔하게 삭제 (CASCADE로 제약 조건까지 정리)
DROP TABLE IF EXISTS public.tarot_history CASCADE;
DROP TABLE IF EXISTS public.tarot_general_messages CASCADE;
DROP TABLE IF EXISTS public.tarot_cards CASCADE;

-- 2. 타로 카드 테이블 생성
CREATE TABLE public.tarot_cards (
  id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  card_number INT NOT NULL UNIQUE,
  name_en TEXT NOT NULL,
  name_ko TEXT NOT NULL,
  meaning_past TEXT NOT NULL,
  meaning_present TEXT NOT NULL,
  meaning_future TEXT NOT NULL,
  is_major BOOLEAN DEFAULT true,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 3. 종합 운명 메시지 템플릿 테이블 생성
CREATE TABLE public.tarot_general_messages (
  id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  content TEXT NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 4. 타로 결과 히스토리 테이블 생성 (CASCADE 옵션 포함)
CREATE TABLE public.tarot_history (
  id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  user_id uuid REFERENCES auth.users(id) ON DELETE CASCADE NOT NULL,
  card1_id uuid REFERENCES public.tarot_cards(id) ON DELETE CASCADE NOT NULL,
  card2_id uuid REFERENCES public.tarot_cards(id) ON DELETE CASCADE NOT NULL,
  card3_id uuid REFERENCES public.tarot_cards(id) ON DELETE CASCADE NOT NULL,
  message_id uuid REFERENCES public.tarot_general_messages(id) ON DELETE CASCADE NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 5. RLS 및 인덱스 설정
ALTER TABLE public.tarot_cards ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Allow public read access to tarot_cards" ON public.tarot_cards FOR SELECT USING (true);

ALTER TABLE public.tarot_general_messages ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Allow public read access to tarot_general_messages" ON public.tarot_general_messages FOR SELECT USING (true);

ALTER TABLE public.tarot_history ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Users can read their own tarot history" ON public.tarot_history FOR SELECT USING (auth.uid() = user_id);
CREATE POLICY "Users can insert their own tarot history" ON public.tarot_history FOR INSERT WITH CHECK (auth.uid() = user_id);
CREATE INDEX idx_tarot_history_user_date ON public.tarot_history (user_id, created_at);

